/*******************************************************************************
 *  function :    EXTI0_1_IRQHandler
 ******************************************************************************/
void
EXTI0_1_IRQHandler(void) {

	/* Is there an interrupt on line 0 */
	if((EXTI->PR & EXTI_PR_PR0) != 0) {

		EXTI->PR |= EXTI_PR_PR0;
		btn_isr(BTN_1);
	}
}
